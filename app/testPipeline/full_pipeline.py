class FullExtractionPipeline:
    """Полный пайплайн извлечения данных"""
    
    def __init__(self):
        self.pattern_extractor = ImprovedExtractor()
        self.ner_enhancer = NEREnhancer()
    
    def process(self, ocr_text: str, source_info: Dict = None) -> Dict:
        """Полная обработка"""
        
        # 1. Извлечение через паттерны
        passport_data = self.pattern_extractor.extract(ocr_text)
        result = self.pattern_extractor.to_dict(passport_data)
        
        # 2. Улучшение через NER
        result = self.ner_enhancer.enhance_data(result, ocr_text)
        
        # 3. Добавление метаданных
        if source_info:
            result['source'] = source_info
        
        # 4. Валидация и предупреждения
        result['validation'] = self._validate(result)
        
        return result
    
    def _validate(self, data: Dict) -> Dict:
        """Проверка полноты данных"""
        warnings = []
        errors = []
        
        # Критичные поля
        if not data.get('passport_number'):
            errors.append("Отсутствует номер паспорта")
        
        if not data.get('product_name'):
            errors.append("Отсутствует название продукта")
        
        # Некритичные поля
        if not data.get('identification', {}).get('factory_number'):
            warnings.append("Заводской номер не найден в документе")
        
        if not data.get('identification', {}).get('issue_date'):
            warnings.append("Дата изготовления не найдена в документе")
        
        return {
            'is_valid': len(errors) == 0,
            'errors': errors,
            'warnings': warnings,
            'completeness': self._calculate_completeness(data)
        }
    
    def _calculate_completeness(self, data: Dict) -> float:
        """Процент заполненности"""
        total_fields = 15  # Общее количество важных полей
        filled_fields = 0
        
        if data.get('passport_number'): filled_fields += 1
        if data.get('product_name'): filled_fields += 1
        if data.get('manufacturer', {}).get('name'): filled_fields += 1
        if data.get('manufacturer', {}).get('address'): filled_fields += 1
        if data.get('technical_specs', {}).get('nominal_voltage'): filled_fields += 1
        if data.get('technical_specs', {}).get('nominal_current'): filled_fields += 1
        if data.get('technical_specs', {}).get('frequency'): filled_fields += 1
        if data.get('technical_specs', {}).get('protection_degree'): filled_fields += 1
        if data.get('technical_specs', {}).get('climate_type'): filled_fields += 1
        if data.get('regulatory_docs', {}).get('tu_number'): filled_fields += 1
        if data.get('regulatory_docs', {}).get('gost_numbers'): filled_fields += 1
        if data.get('lifecycle', {}).get('warranty_period'): filled_fields += 1
        if data.get('lifecycle', {}).get('service_life'): filled_fields += 1
        if data.get('identification', {}).get('issue_date'): filled_fields += 1
        if data.get('identification', {}).get('factory_number'): filled_fields += 1
        
        return round((filled_fields / total_fields) * 100, 2)

